    @SuppressWarnings("empty-statement")
    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        username = txtUsername.getText();
        pass = txtPassword.getText();
        

        if (username.isEmpty() || pass.isEmpty()) {
            lbError.setText("Dữ liệu không được trống !");
            return;
        }

        try {
            Data.Connect();

            Statement s = Data.con.createStatement();
            ResultSet rs = s.executeQuery("Select password from account where username = '" + username + "'");
            rs.next();
            if (rs.getString(1).equals(pass)) {
                Board BanCo = new Board(username);
                BanCo.setVisible(true);
            } else {
                lbError.setText("Mật khẩu không đúng !");
            }

            Data.Close();
        } catch (SQLException ex) {
            //Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            Data.Close();
            lbError.setText("Tên đăng nhập không tồn tại !");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

